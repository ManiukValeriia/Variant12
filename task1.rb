# Метод для обчислення середнього значення елементів масиву
def average(array)
    return 0 if array.empty?  # Перевірка на порожній масив, щоб уникнути ділення на 0
    sum = array.sum           # Знаходження суми елементів масиву
    avg = sum.to_f / array.size # Ділення суми на кількість елементів (перетворення у float для точності)
    avg
  end
  
  # Основна програма
  puts "Введіть елементи масиву через кому (наприклад, 1,2,3):"
  input = gets.chomp           # Отримання введених користувачем даних
  numbers = input.split(",").map(&:to_i)  # Перетворення введених даних у масив цілих чисел
  
  # Обчислення та виведення середнього значення
  average_value = average(numbers)
  puts "Середнє значення елементів масиву: #{average_value}"
  